package dny.apps.tiaw.domain.models.view;

public class DeckViewModel {
	private String id;
	private String name;
	
	public DeckViewModel() {}

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
